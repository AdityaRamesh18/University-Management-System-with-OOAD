    private void course_combo_boxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_course_combo_boxMouseClicked
        try
        {
            // Load the name of each course that the instructor is teaching.
            pst = conn.prepareStatement("select name from course, faculty where "
                                     + "(instructor_prefix = prefix) and "
                                     + "(instructor_fName = fName) and "
                                     + "(instructor_mName = mName) and "
                                     + "(instructor_lName = lName) and "
                                     + "(instructor_suffix = suffix) and "
                                     + "staffNo = ?");
            pst.setString(1, id);
            rs = pst.executeQuery();

            // Load all the courses' names into the combo box.
            String name; // The name of the course
            while (rs.next())
            {
                name = rs.getString("name");
                course_combo_box.addItem(name);
            }
            
            // Load the info of the students who're in the course into the table.
            studentLoad();
        }
        catch (SQLException ex)
        {
            Logger.getLogger(viewStudents.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_course_combo_boxMouseClicked

