    /*
    studentLoad() is a fetch function which retrieves every student's info from the
    MySQL database and renders them in the table.
    Pre-condition: There's at least one student entry in the database.
    Post-condition: The students' info is rendered in the table.
    */
    private void studentLoad()
    {
        try
        {
            // Load the students in each of the instructor's classes' info.
            pst = conn.prepareStatement("select stuID, prefix, fName, mName, lName, "
                                     + "suffix, uniEmail from student s, course c, "
                                     + "course_enrollment_list cel where name = ? "
                                     + "and (c.crn = cel.crn and s.stuID = cel.stuID)");
            pst.setString(1, course_combo_box.getSelectedItem().toString());
            rs = pst.executeQuery();
            
            // Set the number of the info table's rows to 0.
            ResultSetMetaData rsd = rs.getMetaData(); // To get the number of columns from the query
            dtm = (DefaultTableModel)student_info_table.getModel();
            dtm.setRowCount(0);
            
            // Load each student's info into the table.
            while (rs.next())
            {
                Vector vector = new Vector();
                
                for (int index = 1; index <= rsd.getColumnCount(); index++)
                {
                    vector.add(rs.getString("stuID"));
                    vector.add(rs.getString("prefix"));
                    vector.add(rs.getString("fName"));
                    vector.add(rs.getString("mName"));
                    vector.add(rs.getString("lName"));
                    vector.add(rs.getString("suffix"));
                    vector.add(rs.getString("uniEmail"));
                }
                
                dtm.addRow(vector); // Adds a student's info as one of the table's rows
            }
        }
        catch (SQLException ex)
        {
            Logger.getLogger(modifyStudentInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
